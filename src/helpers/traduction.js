export function traduction(text) {
  switch (text) {
    case "actionDeleteProd":
      return "Eliminar productos";
    case "changeStatusProd":
      return "Modificar estado";
    case "changePriceProd":
      return "Modificar precio";
    case "active":
      return "Activo";
    case "paused":
      return "Pausado";
    case "closed":
      return "Finalizado";
  }
}
